    private void btnSalvarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarClienteActionPerformed
      
       if(txtNomeCliente.getText().isEmpty() || txtCPF.getText().isEmpty()|| txtTelefone.getText().isEmpty() || txtCidade.getText().isEmpty() || txtBairro.getText().isEmpty() || txtNascimentoCliente.getText().isEmpty ()){
           
            JOptionPane.showMessageDialog(null,"O campos n√£o devem ficar vazios!","Aviso",JOptionPane.WARNING_MESSAGE);
        }
       else{
        Cliente cliente = new Cliente();
        
        cliente.setNome(txtNomeCliente.getText());
         cliente.setCPF(txtCPF.getText());
        cliente.setTelefone(txtTelefone.getText());
        cliente.setCidade(txtCidade.getText());
        cliente.setBairro(txtBairro.getText());
        cliente.setNascimento(txtNascimentoCliente.getText());
        
        JOptionPane.showMessageDialog(null, "<html>Cliente Cadastrado!<hr>"+"<br>Nome: "+ cliente.getNome()+ 
                                          "<br>CPF: "+cliente.getCPF()+"<br>Telefone:  "+cliente.getTelefone()+"<br> Cidade: "+
                                          cliente.getCidade()+"<html>");
       ClienteDAO clienteDAO = new ClienteDAO();
       clienteDAO.cadastrar(cliente);
       txtNomeCliente.setText("");
       txtCPF.setText("");
       txtTelefone.setText("");
       txtCidade.setText("");
       txtBairro.setText(""); 
       txtNascimentoCliente.setText("");
       
       }
   
       
    }//GEN-LAST:event_btnSalvarClienteActionPerformed

