    private void btnComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprarActionPerformed
        if (txtCodPlanta.getText().isEmpty() || txtCodCliente.getText().isEmpty() || txtQuantidade.getText().isEmpty() || txtValor.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campos não devem ficar vazios!", "Aviso", JOptionPane.WARNING_MESSAGE);
        } else {

            Compra compra = new Compra();
            compra.setCod_planta_compra(txtCodPlanta.getText());
            compra.setCod_cliente_compra(txtCodCliente.getText());
            compra.setQuantidade_compra(Integer.parseInt(txtQuantidade.getText()));
            compra.setValor_compra(Float.parseFloat(txtValor.getText()));

            CompraDAO compraDAO = new CompraDAO();
            compraDAO.cadastrar(compra);
            txtCodPlanta.setText(null);
            txtCodCliente.setText(null);
            txtQuantidade.setText(null);
            txtValor.setText(null);
        }
    }//GEN-LAST:event_btnComprarActionPerformed
     private void ListarDados()
    {
        DefaultTableModel modelo = (DefaultTableModel)tblCompraCliente.getModel();
        ClienteDAO clienteDAO = new ClienteDAO(); // cria um objeto DAO para realizar as transações c/ BD
        modelo.setNumRows(0);//Define q os dados listados iniciam na linha zero na tabela da interface  
        for(Cliente a:clienteDAO.recuperar()) // Laço de repetição que recupera linha a linha os registros
        {
            //adiciona todos os registros linha a linha na tabela da interface 
            modelo.addRow(new Object[] {a.getCod(),a.getNome(),a.getNascimento(),a.getCPF(),a.getTelefone(), a.getCidade(), a.getBairro()}) ;
        }


    }
      private void ListarDados1() {
        DefaultTableModel modelo = (DefaultTableModel) tblPlanta.getModel();
        PlantaDAO plantaDAO = new PlantaDAO(); // cria um objeto DAO para realizar as transações c/ BD
        modelo.setNumRows(0);//Define q os dados listados iniciam na linha zero na tabela da interface  
        for (Planta a : plantaDAO.recuperar()) // Laço de repetição que recupera linha a linha os registros
        {
            //adiciona todos os registros linha a linha na tabela da interface 
            modelo.addRow(new Object[]{a.getCod(), a.getNome(), a.getNomeC(), a.getArea(), a.getQtd(), a.getValor()});
        }
    }
