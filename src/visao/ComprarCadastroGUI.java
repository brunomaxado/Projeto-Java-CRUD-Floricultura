    private void tblPlantaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPlantaMouseClicked
        if(tblPlanta.getSelectedRow()!= -1) //verifica se alguma linha da tabela tblAluno foi selecionada
        {
            if(txtQuantidade.getText().isEmpty())
            {
                 JOptionPane.showMessageDialog(null,"O campo quantidade deve ser preenchido!","Aviso",JOptionPane.WARNING_MESSAGE);
            }
            else{
            txtCodPlanta.setText(tblPlanta.getValueAt(tblPlanta.getSelectedRow(), 0).toString());
             int ij= Integer.parseInt(txtQuantidade.getText());
            float i= Float.parseFloat(tblPlanta.getValueAt(tblPlanta.getSelectedRow(), 5).toString());
            
            txtValor.setText(Float.toString(i*ij));
            }
        
        }
    }//GEN-LAST:event_tblPlantaMouseClicked

