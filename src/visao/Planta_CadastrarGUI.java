    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (txtNome.getText().isEmpty() || txtNomeC.getText().isEmpty() || txtQtd.getText().isEmpty() || txtArea.getText().isEmpty() || txtValor.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campos nÃ£o devem ficar vazios!", "Aviso", JOptionPane.WARNING_MESSAGE);
        } else {
            Planta planta = new Planta();
            int a= Integer.parseInt(txtQtd.getText());
            int b= 2+a;
            System.out.println(b);
            planta.setNome(txtNome.getText());
            planta.setNomeC(txtNomeC.getText());
            planta.setQtd(a);
            planta.setArea(txtArea.getText());
            planta.setValor(Float.parseFloat(txtValor.getText()));
            JOptionPane.showMessageDialog(null, "<html>Planta Cadastrada!<hr>" + "<br>Nome Popular: " + planta.getNome()
                    + "<br>Nome cientifico: " + planta.getNomeC() + "<br>Area: " + planta.getArea() + "<br> Quantidade: "
                    + planta.getQtd() +"<br> Valor:"+ planta.getValor() +"<html>");

            PlantaDAO plantaDAO = new PlantaDAO();
            plantaDAO.cadastrar(planta);
            txtNome.setText(null);
            txtNomeC.setText(null);

            txtArea.setText(null);

            txtQtd.setText(null);
            txtValor.setText(null);

        }
    }//GEN-LAST:event_btnSalvarActionPerformed

